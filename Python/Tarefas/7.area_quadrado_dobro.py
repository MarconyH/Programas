##Faça um programa que calcule a área de um quadrado, em seguida mostre o dobro da área para o usuário.

base= float(input("Informe a base do quadrado: "))
altura= float(input("Informe a altura do quadrado: "))

area= base*altura
area2x= area*2

print ("Área do quadrado:",area)
print ("Dobro da área do quadrado:",area2x)

